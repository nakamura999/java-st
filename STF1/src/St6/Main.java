package St6;

public class Main {
	public static void main(String[] args) {
		double one, two, three;
		double sum, avg;
		one = 1.2;
		two = 3.7;
		three = 4.1;
		System.out.println(one + " " + two + " " + three);
		sum = one + two + three; // 合計値
		avg = sum / 3.0; // 平均値
		System.out.println("合計値: " + sum);
		System.out.println("平均値: " + avg);
	}

}
