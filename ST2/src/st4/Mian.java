// if文
package st4;

public class Mian {
	public static void main(String[] args) {
		if (true) {
			System.out.println("条件式がtrueの為、出力されます");
		}

		if (false) {
			System.out.println("条件式がfalseの為、出力されません");
		}

		int x = 5;
		if (x > 2) {
			System.out.println("xは２より大きい");
		}

		if (x > 8) {
			System.out.println("xは８より大きい");
		}
	}

}
